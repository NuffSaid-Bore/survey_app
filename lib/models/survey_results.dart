class SurveyResults {
  final int totalSurveys;
  final double averageAge;
  final int oldestAge;
  final int youngestAge;
  final double pizzaPercentage;
  final double eatOutRating;

  SurveyResults({
    required this.totalSurveys,
    required this.averageAge,
    required this.oldestAge,
    required this.youngestAge,
    required this.pizzaPercentage,
    required this.eatOutRating,
  });
}
